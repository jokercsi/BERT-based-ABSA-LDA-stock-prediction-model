import yfinance as yf
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

#S&P 500 데이터 확인
SnP = yf.Ticker('^GSPC')
Nasdaq = yf.Ticker('^IXIC')
Dow = yf.Ticker('^DJI')

SnP.history(period='5d', actions=False)

Nasdaq.history(period='5d', actions=False)

Dow.history(period='5d', actions=False)

df1 = SnP.history(start='2017-12-17', end='2020-7-18', actions=False)

df2 = Nasdaq.history(start='2017-12-17', end='2020-7-18', actions=False)

df3 = Dow.history(start='2017-12-17', end='2020-7-18', actions=False)

df1

df1.reset_index(inplace=True)
df2.reset_index(inplace=True)
df3.reset_index(inplace=True)

df1['Date'] = df1['Date'].dt.strftime('%Y/%m/%d')
df2['Date'] = df2['Date'].dt.strftime('%Y/%m/%d')
df3['Date'] = df3['Date'].dt.strftime('%Y/%m/%d')

df1

df1.to_csv('SnP.csv',index=False)
df2.to_csv('Nasdaq.csv',index=False)
df3.to_csv('Dow.csv',index=False)